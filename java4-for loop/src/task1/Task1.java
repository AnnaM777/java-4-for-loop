package task1;
//Determine the sum of numbers from 0 to 20. (For loop)
public class Task1 {
    public static void main(String[] args) {


        int sum = 0;

        for (int i = 0; i <= 20; i++){
            sum = sum + i;
        }
        System.out.println("The sum of the numbers from 1 to 20 is " + sum + ".");


    }
}
